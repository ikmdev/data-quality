-- PostgreSQL schema for PIQI Tools
-- Verify-first and additive updates for evaluation run + assessment results.

CREATE TABLE IF NOT EXISTS public.piqi_evaluation_run
(
    id bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    run_name character varying(100) NOT NULL,
    piqi_model_mnemonic character varying(100) NOT NULL,
    evaluation_rubric_mnemonic character varying(100) NOT NULL,
    status character varying(20) NOT NULL DEFAULT 'IN_PROGRESS',
    total_evaluations bigint DEFAULT 0,
    total_records bigint DEFAULT 0,
    total_completed bigint DEFAULT 0,
    total_failed bigint DEFAULT 0,
    created_at timestamp with time zone DEFAULT now(),
    completed_at timestamp with time zone,
    CONSTRAINT pk_piqi_evaluation_run PRIMARY KEY (id),
    CONSTRAINT piqi_evaluation_run_run_name_key UNIQUE (run_name)
);

CREATE TABLE IF NOT EXISTS public.piqi_evaluation_results
(
    id bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    run_id bigint NOT NULL,
    row_num integer,
    message_id text NOT NULL,
    data_class character varying(50) NOT NULL,
    attribute_name text NOT NULL,
    attribute_value text,
    assessment character varying(50),
    status character varying(50),
    reason text,
    effect text,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    CONSTRAINT pk_piqi_evaluation_results PRIMARY KEY (id),
    CONSTRAINT fk_results_to_run FOREIGN KEY (run_id)
        REFERENCES public.piqi_evaluation_run (id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE CASCADE
);

