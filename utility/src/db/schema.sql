DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint c
        WHERE c.conname = 'piqi_evaluation_run_run_name_key'
          AND c.conrelid = 'public.piqi_evaluation_run'::regclass
    ) THEN
        ALTER TABLE public.piqi_evaluation_run
            ADD CONSTRAINT piqi_evaluation_run_run_name_key UNIQUE (run_name);
    END IF;
END
$$;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint c
        JOIN pg_attribute a
          ON a.attrelid = c.conrelid
         AND a.attnum = ANY (c.conkey)
        WHERE c.contype = 'f'
        AND c.conrelid = 'public.piqi_evaluation_results'::regclass
          AND c.confrelid = 'public.piqi_evaluation_run'::regclass
          AND a.attname = 'run_id'
    ) THEN
        ALTER TABLE public.piqi_evaluation_results
            ADD CONSTRAINT fk_results_to_run
            FOREIGN KEY (run_id)
            REFERENCES public.piqi_evaluation_run (id)
            ON UPDATE NO ACTION
            ON DELETE CASCADE;
    END IF;
END
$$;

